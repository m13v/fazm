import Foundation

/// Provides the bundled Deepgram API key at runtime using XOR deobfuscation.
/// Not cryptographically secure — prevents casual string extraction from the binary.
enum DeepgramKeyProvider {

    static func deobfuscate() -> String {
        let bytes = zip(e, k).map { $0 ^ $1 }
        return String(bytes: bytes, encoding: .utf8) ?? ""
    }

